#include "abilities.h"
#include <time.h>
//---------------------------------------------------------------------------
ability::ability()
{
	id = 0;
}
//---------------------------------------------------------------------------
ability::ability(int a)
{
	id = a;
}
//---------------------------------------------------------------------------
int ability::get_a()
{
	srand(time(NULL));
	int Tid;
	Tid = rand();
	Tid = id % 4;
	Tid++;
	char typ = 'm';
	if (id < 3) {
		typ = 'a';
	}
	return Tid;
}
//---------------------------------------------------------------------------
int ability::get_id()
{
	return id;
}
