#include "curtain.h"
//---------------------------------------------------------------------------
rido::rido()
{
	scen = 0;
	active = 0;
}
//---------------------------------------------------------------------------
void rido::set_scen(int a)
{
	scen = a;
}
//---------------------------------------------------------------------------
void rido::set_active(int a)
{
	active = a;
}
//---------------------------------------------------------------------------
void rido::set_m(int a, char st)
{
	if (st == 'x') {
		pilar.set_cx(a);
	}
	else {
		pilar.set_cy(a);
	}
}
//---------------------------------------------------------------------------
void rido::set_m(int a, int b)
{
	pilar.set_cx(a);
	pilar.set_cy(b);
}
//---------------------------------------------------------------------------
int rido::get_scen()
{
	return scen;
}
//---------------------------------------------------------------------------
int rido::get_active()
{
	return active;
}
//---------------------------------------------------------------------------
int rido::get_m(char st)
{
	if (st == 'x') {
		return pilar.get_cx();
	}
	else {
		return pilar.get_cy();
	}
}
